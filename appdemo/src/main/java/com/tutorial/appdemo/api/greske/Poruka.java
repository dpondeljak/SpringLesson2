package com.tutorial.appdemo.api.greske;

public class Poruka {
	private String poruka;
	private String tip;
	private int kod;

	public Poruka() {
	}

	public String getPoruka() {
		return poruka;
	}

	public void setPoruka(String poruka) {
		this.poruka = poruka;
	}

	public String getTip() {
		return tip;
	}

	public void setTip(String tip) {
		this.tip = tip;
	}

	public int getKod() {
		return kod;
	}

	public void setKod(int kod) {
		this.kod = kod;
	};

}
